s3 = boto3.client("s3")

def test_upload(url, label):
    boundary = "----FormBound" + label.replace(" ", "")
    body = ("--" + boundary + "\r\n"
            "Content-Disposition: form-data; name=file; filename=test.jpg\r\n"
            "Content-Type: image/jpeg\r\n\r\n").encode("utf-8")
    body += jpeg
    body += ("\r\n--" + boundary + "--\r\n").encode("utf-8")

    req = urllib.request.Request(
        url, data=body,
        headers={"Content-Type": "multipart/form-data; boundary=" + boundary}
    )
    resp = urllib.request.urlopen(req)
    result = json.loads(resp.read().decode("utf-8"))
    url_out = result.get("foto_url", "")
    m = re.search(r"reportes/([^?]+)", url_out)
    if m:
        key = "reportes/" + m.group(1)
        s3.download_file("incendios-valle-sol", key, "/tmp/" + label + ".jpg")
        with open("/tmp/" + label + ".jpg", "rb") as f:
            data = f.read()
        print(f"{label}: {len(data)} bytes, first {data[:4].hex()}, valid={data[:2] == bytes([0xFF, 0xD8])}")

test_upload("http://localhost:8000/reports/upload", "direct_api")
test_upload("http://localhost/api/reports/upload", "via_nginx")
