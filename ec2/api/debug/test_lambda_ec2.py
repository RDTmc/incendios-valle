#!/usr/bin/env python3
"""Test Lambda from EC2 with known-good JPEG, then verify output."""
import base64, json, boto3, re, sys

# Minimal valid JPEG (gray pixel)
jpeg = bytes([
    0xFF, 0xD8, 0xFF, 0xE0, 0x00, 0x10, 0x4A, 0x46, 0x49, 0x46, 0x00, 0x01,
    0x01, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x00,
    0xFF, 0xDB, 0x00, 0x43, 0x00, 0x08, 0x06, 0x06, 0x07, 0x06, 0x05, 0x08,
    0x07, 0x07, 0x07, 0x09, 0x09, 0x08, 0x0A, 0x0C, 0x14, 0x0D, 0x0C, 0x0B,
    0x0B, 0x0C, 0x19, 0x12, 0x13, 0x0F, 0x14, 0x1D, 0x1A, 0x1F, 0x1E, 0x1D,
    0x1A, 0x1C, 0x1C, 0x20, 0x24, 0x2E, 0x2E, 0x27, 0x22, 0x24, 0x2C, 0x23,
    0x1C, 0x1C, 0x28, 0x37, 0x29, 0x2C, 0x30, 0x31, 0x34, 0x34, 0x34, 0x1F,
    0x27, 0x39, 0x3D, 0x38, 0x32, 0x3C, 0x2E, 0x33, 0x34, 0x32,
    0xFF, 0xC0, 0x00, 0x11, 0x08, 0x00, 0x01, 0x00, 0x01, 0x03, 0x01, 0x22,
    0x00, 0x02, 0x11, 0x01, 0x03, 0x11, 0x01,
    0xFF, 0xC4, 0x00, 0x1F, 0x00, 0x00, 0x01, 0x05, 0x01, 0x01, 0x01, 0x01,
    0x01, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04,
    0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10,
    0xFF, 0xD9
])

print(f"Input JPEG: {len(jpeg)} bytes, starts with {jpeg[:4].hex()}")

b64 = base64.b64encode(jpeg).decode("utf-8")
payload = json.dumps({"body": b64, "content_type": "image/jpeg"})

client = boto3.client("lambda")
response = client.invoke(
    FunctionName="upload-proxy",
    InvocationType="RequestResponse",
    Payload=payload
)
result = json.loads(response["Payload"].read().decode("utf-8"))
print(f"Lambda status: {result.get('statusCode')}")
body = json.loads(result["body"])
url = body.get("foto_url", "N/A")
print(f"URL: {url[:120]}...")

m = re.search(r"reportes/([^?]+)", url)
if m:
    key = "reportes/" + m.group(1)
    print(f"S3 key: {key}")
    s3 = boto3.client("s3")
    s3.download_file("incendios-valle-sol", key, "/tmp/lambda_test_out.jpg")
    with open("/tmp/lambda_test_out.jpg", "rb") as f:
        data = f.read()
        print(f"Output JPEG: {len(data)} bytes")
        print(f"First 16 bytes: {data[:16].hex()}")
        print(f"Valid JPEG header: {data[:2] == bytes([0xFF, 0xD8])}")

        rej_count = sum(1 for i in range(len(data)-2) if data[i:i+3] == bytes([0xEF, 0xBF, 0xBD]))
        print(f"U+FFFD patterns: {rej_count}")
else:
    print("Could not extract key from URL")
    sys.exit(1)
